package dao;

/** Represents country of a project.
 * @author Vaibhav, Felipe, Gagandeep, Gurpreet
 * @version 1.0
 * @since 1.0
*/
public class Country {
	/** name of the country.
   	 * @author Vaibhav, Felipe, Gagandeep, Gurpreet
   	 * @version 1.0
   	 * @since 1.0
   	*/
	public String name;

	
	/** sets name of the country.
   	 * @author Vaibhav, Felipe, Gagandeep, Gurpreet
   	 * @version 1.0
   	 * @return name of the country
   	 * @since 1.0
   	*/
	public String getName() {
		return name;
	}
	/** gets name of the country.
   	 * @author Vaibhav, Felipe, Gagandeep, Gurpreet
   	 * @version 1.0
   	 * @param name of the country
   	 * @since 1.0
   	*/
	public void setName(String name) {
		this.name = name;
	}

}
